package Modelo;

public class DatosPartido {
    double porcentajes1;
    double porcentajes2;
    double porcentajes3;

    public DatosPartido(double porcentajes1, double porcentajes2, double porcentajes3) {
        this.porcentajes1 = porcentajes1;
        this.porcentajes2 = porcentajes2;
        this.porcentajes3 = porcentajes3;
    }

    public double getPorcentajes1() {
        return porcentajes1;
    }

    public double getPorcentajes2() {
        return porcentajes2;
    }

    public double getPorcentajes3() {
        return porcentajes3;
    }
    
    
    
    
}

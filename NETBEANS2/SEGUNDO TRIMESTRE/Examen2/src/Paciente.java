public class Paciente {
    private String nombre;
    private int telefono;
    public Paciente(){
        this.nombre= nombre;
        this.telefono= telefono;
    }
    public void setNombre(String n){
        nombre=n;
    }
    public void setTelefono(int n){
        telefono=n;
    }
    public String getNombre(){
        return nombre;
    }
    public int getTelefono(){
        return telefono;
    }
}

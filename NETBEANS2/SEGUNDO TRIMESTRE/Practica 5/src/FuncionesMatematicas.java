    private void calcularprimosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularprimosActionPerformed
        // TODO add your handling code here:
        int numdesde=Integer.parseInt(desde.getText());
        int numhasta=Integer.parseInt(hasta.getText());
        
        for(int i=numdesde;i<=numhasta;i++){
            if(Mate.primo(i)){
                areaprimos.append(i+"\n");
            }
        }
        
    }//GEN-LAST:event_calcularprimosActionPerformed


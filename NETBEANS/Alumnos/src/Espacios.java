    public void separarPalabras(){
        String palabras = taAnalizar.getText(); // extraigo el texto
        char separador = ' ';
        int contador = 1;
        if(jComboBox1.getSelectedIndex()==0){
            separador = '.';
        }
        if(jComboBox1.getSelectedIndex()==1){
            separador = ';';
        }
        if(jComboBox1.getSelectedIndex()==2){
            separador = ',';
        }
        if(jComboBox1.getSelectedIndex()==3){
            separador = ' ';
        }
        
        for(int i=0; i<palabras.length();i++){
            if(palabras.charAt(i)==separador){
                taResultado.append("\n");
                contador++;
                
            }else{
                char palabra = palabras.charAt(i);
                String cadena=(new StringBuffer().append(palabra)).toString();
                taResultado.append(cadena);
            }
        }
        
        tfnpalabras.setText(contador + "");
      
    }


    public void pasarpalabras(){
        String palabras=area.getText();
        char separador=' ';
        int cont=1;
        int comprobador=0;
        if (combo.getSelectedIndex()==3){
            separador=' ';
        }
        if (combo.getSelectedIndex()==2){
            separador=',';
        }
        if (combo.getSelectedIndex()==1){
            separador=';';
        }
        if (combo.getSelectedIndex()==0){
            separador='.';
        }
        for (int i=0;i<palabras.length();i++){
            if(palabras.charAt(i)==separador){
                area2.append("\n");
                comprobador++;
                cont++;
            }else{
              char palabra = palabras.charAt(i);
                String cadena=(new StringBuffer().append(palabra)).toString();
                area2.append(cadena);
                
            }
        }
        if(comprobador==0){
           label.setText("AVISO: SEPARADOR NO ENCONTRADO O INCORRECTO");
           
           
        }
        numero.setText(cont+"");
        
    }
    
